 function dropdown(){
    drop = document.querySelector("#dropdown").style.display = "block";
    return drop
}
 function upDropDown(){
    upDrop = document.querySelector("#dropdown").style.display = "none";
    return upDrop;
}

function navber(){
    navshow = document.querySelector("#navber").style.display = "inline-block"
    return navshow;
}

function closeBar(){
    barClose = document.querySelector('#navber').style.display = 'none'
    return barClose;
}


